<h2>How do I contact the embassy of Finland in Vienna?</h2>
<p>The embassy of Finland in Vienna is located at Opernring 3-5, 6. Stock and can be contacted by telephone on <a href="tel:+4315350365" class="contact-details__tel">(1) 535 03 65</a> and by email <a href="mailto:sanomat.wie@formin.fi">sanomat.wie@formin.fi</a>. The consular section is located at Opernring 3-5 Top 601 and can be contacted by telephone on <a href="tel:+4316055555" class="contact-details__tel">(1) 605 55 55</a>.</p> <h3>When is the embassy open?</h3>
<p> The embassy of Finland in Vienna is open Monday to Friday from 09:00 to 12:00. The consular section is open weekdays from 09:00 to 12:00. The offices are typically closed during the weekend and during holidays. Make sure to contact the embassy to confirm opening hours before visiting their offices.<br></p>
